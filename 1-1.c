#include <stdio.h>
#include <math.h>

/**
* @brief расчитывает значение функции А
* @param x значение переменной x
* @param y значение переменной y
* @param z значение переменной z
* @return расчитывает значение
*/
double getA(double const x, double const y, double const z);

/**
* @brief расчитывает значение функции B
* @param x значение переменной x
* @param y значение переменной y
* @param z значение переменной z
* @return расчитывает значение
*/
double getB(double const x, double const y, double const z);
/**
* @brief точка входа в программу
* @return возвращает 0 если программа выполнена корректно, иначе 1
*/
int main(void)
{
	double const x = 0.61;
	double const y = 0.9;
	double const z = 0.3;
	/** вывод с нужной стороны */
	printf(" x = %2f/n", x);
	printf(" y = %2f/n", y);
	printf(" z = %2f/n/n", z);
	printf("A = %6f/n", getA(x, y, z));
	printf("B = %6f/n", getB(x, y, z));
	return 0;
}
double getA(double const x, double  const y, double const z)
{
	return (pow(z, 2 * x) + pow(y, -x) * cos(z + y) * x) / x + 1;
}
double getB(double const x, double const y, double const z)
{
	return sqrt((pow(x, 2)) + y) - pow(y, 2) * pow(sin((x + z) / x), 3);
}
